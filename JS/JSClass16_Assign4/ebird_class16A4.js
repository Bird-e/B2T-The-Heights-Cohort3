//array of numbers

var nums = [5, 2, 10, 0, 50, 1900, 25, 25];
//sum of the numbers
var sum = 0;
//initialize, condition, incrementation
for(var i = 0; i < nums.length; i++){
	//add all the numbers together
	sum += nums[i]; 
}
//display the sum of all the numbers in the console.
console.log(sum);

